    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = usernameTxt.getText().trim();
        String password = passwordTxt.getText();
        Map<String, User> userList = ChatRoom.getChatRoom().getUserList();
        Set<Map.Entry<String, User>> entrySet = userList.entrySet();
        User user = null;
        boolean isUser = false;
        for (Map.Entry<String, User> m : entrySet) {
            user = m.getValue();
            if (user.getPassword().equals(password) && user.getUsername().equals(username)) {
                isUser = true;
                break;
            }
        }
        if (isUser) {
            this.setVisible(false);
            new Home(user).setVisible(true);
        } else {
            ImageIcon icon = new ImageIcon("src/images/login.png");
            JOptionPane.showMessageDialog(SignIn.this, "Invalid username password combination", "Error", JOptionPane.INFORMATION_MESSAGE, icon);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

