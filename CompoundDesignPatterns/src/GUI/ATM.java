    private void clearBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearBtnActionPerformed
        if (isPasswordTypable && !isAmmountTypable) {
            String password = passwordTxt.getText().trim();
            if (password.length() >= 1) {
                String substring = password.substring(0, password.length() - 1);
                passwordTxt.setText(substring);
            }
        } else if (isAmmountTypable && !isPasswordTypable) {
            String ammount = ammountTxt.getText().trim();
            if (ammount.length() >= 1) {
                String substring = ammount.substring(0, ammount.length() - 1);
                ammountTxt.setText(substring);
            }
        }
    }//GEN-LAST:event_clearBtnActionPerformed

