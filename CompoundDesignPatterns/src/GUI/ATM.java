    private void enterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterBtnActionPerformed
        if (isPasswordTypable && !isAmmountTypable) {
            if (currentCard.getCardPassword().equals(passwordTxt.getText().trim())) {
                currentLayout = "mainMenu";
                CARD_LAYOUT.show(display, "mainMenu");
                passwordTxt.setText(null);
            } else {
                message("Invalid Password. Check your card pin number", "Error");
            }
        } else if (isAmmountTypable && !isPasswordTypable) {
            int ammount = Integer.parseInt(ammountTxt.getText().trim());
            if (ammount > 0) {
                this.ammount = ammount;
                currentLayout = "receipt";
                CARD_LAYOUT.show(display, "receipt");
            } else {
                message("Ammount Must be larger than 0", "Error");
            }
        }

    }//GEN-LAST:event_enterBtnActionPerformed
    private void message(String message, String title) {
        boolean defaultLFDecorated = JDialog.isDefaultLookAndFeelDecorated();
        try {
            JDialog.setDefaultLookAndFeelDecorated(true);
            JOptionPane pane = new JOptionPane(message, JOptionPane.ERROR_MESSAGE, JOptionPane.DEFAULT_OPTION);
            JDialog dialog = pane.createDialog(display, title);
            dialog.getRootPane().setWindowDecorationStyle(JRootPane.NONE);
            pane.setOpaque(false);
            ArrayList<Component> components = new ArrayList<Component>(Arrays.asList(pane.getComponents()));
            while (!components.isEmpty()) {
                Component c = components.remove(0);
                if (c instanceof JComponent) {
                    ((JComponent) c).setOpaque(false);
                }

                if (c instanceof Container) {
                    components.addAll(Arrays.asList(((Container) c).getComponents()));
                }
            }

            dialog.setVisible(true);
            dialog.dispose();
        } finally {
            JDialog.setDefaultLookAndFeelDecorated(defaultLFDecorated);
        }
    }
