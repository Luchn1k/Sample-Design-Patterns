    private void setShot(int clickX, int clickY) {
        if (isClicable) {
            newAudioClip.play();
            shot.show();
            shot.setBounds(clickX - 16, clickY - 16, 32, 32);
            currenTargetLocation = target.getBounds();
            currentClickLocation = shot.getBounds();
            clickCount++;
            if (hitLocation == null) {
                hitLocation = new HitLocation(currentClickLocation, currenTargetLocation);
            }
            hitLocation.setHitLocation(currentClickLocation);
            hitLocation.setTargetLocation(currenTargetLocation);
            hitLocation.save();
        }
    }

