    private void startGame() {
        hitLocation = null;
        if (PnlColor.getColor() != null) {
            gamePnl.setBackground(PnlColor.getColor());
        }
        shot.hide();
        target.hide();
        if (gameThread != null) {
            if (gameThread.isAlive()) {
                gameThread.stop();
                reset();

            }
        }
        summeryPnl.setVisible(false);
        int targetCount = Integer.parseInt(targetCountTxt.getText().trim());
        int targetSpeed = Integer.parseInt(targetSpeedTxt.getText().trim());

        gameThread = new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = COUNTDOWN; i >= 0; i--) {
                    gameStartLbl.setText(COUNTDOWNTEXT + i + "...");
                    gameStartLbl.setVisible(true);
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Game.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                gameStartLbl.setVisible(false);
                target.setCursor(customCursorAim);
                gamePnl.setCursor(customCursorAim);
                isClicable = true;
                for (int i = 0; i < targetCount; i++) {
                    randomTargetGenarator();
                    try {
                        Thread.sleep(targetSpeed);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Game.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                summeryPnl.setVisible(true);
                countDataLbl.setText("You Hit " + hitCount + " out of " + targetCount + " by using " + clickCount + " Shots");
                DecimalFormat df = new DecimalFormat("##.##");
                accuracyDataTxt.setText("Your Accuracy is  " + df.format(calculateAcuracy(targetCount)) + "%");
                reset();
            }
        });
        gameThread.start();
    }
