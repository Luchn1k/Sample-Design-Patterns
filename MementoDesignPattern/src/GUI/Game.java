    private void reset() {
        target.hide();
        shot.hide();
        clickCount = 0;
        hitCount = 0;
        isClicable = false;
        target.setCursor(customCursorNone);
        gamePnl.setCursor(customCursorNone);
    }

    private double calculateAcuracy(int targetCount) {
        int originalClicks = clickCount > targetCount ? clickCount : targetCount;
        return ((Double.valueOf(hitCount) / Double.valueOf(originalClicks)) * Double.valueOf(100));

    }

